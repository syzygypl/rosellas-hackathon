async function scaffoldFoundationsSection(page, tokenDocument, kitManifest, xOffset, yOffset) {
  const root = createSectionFrame('Repository Foundations', xOffset, yOffset, 1200, 900);
  root.setPluginData('rosellasGenerated', 'true');
  page.appendChild(root);

  const colorCollection = tokenDocument.collections.find((collection) => collection.name === COLOR_COLLECTION_NAME);
  const colorTokens = colorCollection ? colorCollection.modes[0].tokens : [];
  const colorSection = createSectionFrame('Colors', 32, 72, 1096, 360);
  root.appendChild(colorSection);

  let x = 24;
  let y = 72;
  for (const token of colorTokens) {
    await createColorSwatch(colorSection, token, x, y);
    x += 168;
    if (x > 900) {
      x = 24;
      y += 96;
    }
  }

  const typeSection = createSectionFrame('Typography', 32, 464, 520, 360);
  root.appendChild(typeSection);
  let typeY = 72;
  for (const style of kitManifest.textStyles) {
    await createTextRow(typeSection, style, 24, typeY);
    typeY += 36;
  }

  const spacingSection = createSectionFrame('Spacing', 592, 464, 536, 360);
  root.appendChild(spacingSection);
  const spacingCollection = tokenDocument.collections.find((collection) => collection.name === 'Spacing');
  let spacingY = 72;
  for (const token of spacingCollection.modes[0].tokens) {
    await createSpacingRow(spacingSection, token, 24, spacingY);
    spacingY += 24;
  }

  return root;
}

