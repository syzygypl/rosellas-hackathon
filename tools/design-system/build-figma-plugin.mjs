async function buildButtonComponents(parent, x, y) {
  const variants = [
    { tone: 'Primary', size: 'Small', state: 'Default' },
    { tone: 'Primary', size: 'Medium', state: 'Default' },
    { tone: 'Primary', size: 'Large', state: 'Default' },
    { tone: 'Primary', size: 'Medium', state: 'Hover' },
    { tone: 'Primary', size: 'Medium', state: 'Focus' },
    { tone: 'Primary', size: 'Medium', state: 'Disabled' },
    { tone: 'Ghost', size: 'Medium', state: 'Default' }
  ];
  const dimensions = {
    Small: { width: 148, height: 36, paddingX: 18, radius: 8 },
    Medium: { width: 188, height: 44, paddingX: 28, radius: 8 },
    Large: { width: 448, height: 58, paddingX: 96, radius: 12 }
  };
  const components = [];

  for (const variant of variants) {
    const size = dimensions[variant.size];
    const isGhost = variant.tone === 'Ghost';
    const isDisabled = variant.state === 'Disabled';
    const fill = isGhost ? '#ffffff' : variant.state === 'Hover' ? '#111110' : '#222220';
    const textColor = isGhost ? '#222220' : '#ffffff';
    const component = createAutoComponent('Tone=' + variant.tone + ', Size=' + variant.size + ', State=' + variant.state, size.width, size.height, {
      direction: 'HORIZONTAL',
      gap: 10,
      paddingX: size.paddingX,
      paddingY: 0,
      fill,
      stroke: variant.state === 'Focus' ? '#356fd9' : isGhost ? '#ded9d2' : fill,
      radius: size.radius,
      align: 'CENTER',
      justify: 'CENTER'
    });

    component.opacity = isDisabled ? 0.42 : 1;

    if (variant.size === 'Large') {
      await createText(component, '->', 0, 0, {
        fontSize: 13,
        fontStyle: 'Bold',
        color: textColor
      });
    }

    await createText(component, isGhost ? 'LEARN MORE' : 'ENTER WORKSPACE', 0, 0, {
      fontSize: variant.size === 'Small' ? 11 : 12,
      fontStyle: 'Bold',
      color: textColor,
      uppercase: true
    });

    parent.appendChild(component);
    components.push(component);
  }

  placeVariantSet(parent, 'Button', components, x, y);
}

