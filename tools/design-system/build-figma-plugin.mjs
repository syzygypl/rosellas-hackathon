function applyAutoLayout(node, options) {
  node.layoutMode = options.direction || 'HORIZONTAL';
  node.primaryAxisSizingMode = options.primarySizing || 'FIXED';
  node.counterAxisSizingMode = options.counterSizing || 'FIXED';
  node.primaryAxisAlignItems = options.justify || 'MIN';
  node.counterAxisAlignItems = options.align || 'CENTER';
  node.itemSpacing = options.gap || 0;
  node.paddingLeft = options.paddingX || 0;
  node.paddingRight = options.paddingX || 0;
  node.paddingTop = options.paddingY || 0;
  node.paddingBottom = options.paddingY || 0;
}

function applyBoxStyles(node, options) {
  node.fills = options.fill === null ? [] : [{ type: 'SOLID', color: hexToRgb(options.fill || '#ffffff') }];
  node.strokes = options.stroke === null ? [] : [{ type: 'SOLID', color: hexToRgb(options.stroke || '#e1ddd7') }];
  node.strokeWeight = options.stroke === null ? 0 : 1;
  node.cornerRadius = options.radius || 0;
}

